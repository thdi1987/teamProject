package report.domain;

public class reportHistoryVO {
	
	private int RHNO;
	private String RHID;
	private String RHEMAIL;
	private String RHCOUNT;
	private String RHDATE;
	private String RHRELEASEDATE;
	
	reportHistoryVO(){}
	
	
	public int getRHNO() {
		return RHNO;
	}
	public void setRHNO(int rHNO) {
		RHNO = rHNO;
	}
	public String getRHID() {
		return RHID;
	}
	public void setRHID(String rHID) {
		RHID = rHID;
	}
	public String getRHEMAIL() {
		return RHEMAIL;
	}
	public void setRHEMAIL(String rHEMAIL) {
		RHEMAIL = rHEMAIL;
	}
	public String getRHCOUNT() {
		return RHCOUNT;
	}
	public void setRHCOUNT(String rHCOUNT) {
		RHCOUNT = rHCOUNT;
	}
	public String getRHDATE() {
		return RHDATE;
	}
	public void setRHDATE(String rHDATE) {
		RHDATE = rHDATE;
	}
	public String getRHRELEASEDATE() {
		return RHRELEASEDATE;
	}
	public void setRHRELEASEDATE(String rHRELEASEDATE) {
		RHRELEASEDATE = rHRELEASEDATE;
	}
	
	
	
	
	
	
	

}
